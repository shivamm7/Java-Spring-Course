class Hello{
    public static void main(String a[]){
        // literals

        int num1 = 10;
        int num2 = 0b101;
        System.out.println(num2);

        num2 = 0x7E;
        System.out.println(num2);

        num1 = 10_000_000;
        System.out.println(num1);

        double num3 = 56e10;
        System.out.println(num3);

        char c = 'a';
        System.out.println(c);
        c++;
        System.out.println(c);


    }
}